#O dicionario serve exatamente como um dicionario de informações. Você coloca a chave e valores e ele vai trazer as informações daquela chave.
# 


dicionario = {
    "Enio": "Um caba maneirão, tú é doido",
    "David": "Meu segundo nome e bem mais bonito",
    "Resumo": "É literalmente um dicionario, contendo uma chave e a informação dessa chave",
    }

print(dicionario)

for chave in dicionario:
    print(chave)